#include <stdio.h>

int main()
{
	int a = +4;	//+号可以是一元运算符，表正数 
	a = -4;		//-号同样可以，表负数 
	double b = 12 / 5;		//表达式12/5实际上是整数除法，运算结果也是整数2 
	printf("%f\n",b);	
	b = 12.0 / 5;	//必须要有至少一个数是double这样才是浮点数除法
	printf("%f\n",b);
	 
	int c = (3 + 4) * 5 - 7 / 4;
	//加减乘除先后顺序都懂的，不用多说
	c = (3, 5, 8);	//逗号运算符是一个二元运算符，结果值为逗号右边的值
	//由于逗号运算符优先级比赋值运算符低，所以要用括号扩起
	//3,5,8 的结果值是8 
	printf("c = %d\n",c);
}
