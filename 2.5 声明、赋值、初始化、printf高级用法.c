#include <stdio.h>

int main()
{
	int a;				//声明: 创建一个整数变量a 
	a = 1;				//赋值: 令a等于1  
	int b = 2;  		//初始化: 在声明变量的同时为其赋值 
	int c = 3, d = 4;	//多变量声明和赋值: 用逗号隔开一个变量 
	printf("%d ",a);	//格式化输出: %d表示要输出的变量类型为int, 然后需要在后面添加要输出的变量 
	printf("%d %d %d\n",b,d,c);	//根据字符串内格式输出符号的个数和顺序来决定要输出变量的顺序 
	return 0; 
}
